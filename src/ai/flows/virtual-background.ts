'use server';
/**
 * @fileOverview Allows users to replace their real-world backgrounds with virtual backgrounds during video calls.
 *
 * - applyVirtualBackground - A function that applies a virtual background to a user's video feed.
 * - ApplyVirtualBackgroundInput - The input type for the applyVirtualBackground function.
 * - ApplyVirtualBackgroundOutput - The return type for the applyVirtualBackground function.
 */

import {ai} from '@/ai/genkit';
import {z} from 'genkit';
import * as fs from 'fs';

const ApplyVirtualBackgroundInputSchema = z.object({
  photoDataUri: z
    .string()
    .describe(
      'A photo of the user, as a data URI that must include a MIME type and use Base64 encoding. Expected format: \'data:<mimetype>;base64,<encoded_data>\'.')
  ,virtualBackgroundDataUri: z
    .string()
    .describe(
      'A photo of the virtual background, as a data URI that must include a MIME type and use Base64 encoding. Expected format: \'data:<mimetype>;base64,<encoded_data>\'.'    )
});
export type ApplyVirtualBackgroundInput = z.infer<typeof ApplyVirtualBackgroundInputSchema>;

const ApplyVirtualBackgroundOutputSchema = z.object({
  modifiedPhotoDataUri: z.string().describe('A photo of the user with the virtual background, as a data URI that must include a MIME type and use Base64 encoding. Expected format: \'data:<mimetype>;base64,<encoded_data>\'.'),
});
export type ApplyVirtualBackgroundOutput = z.infer<typeof ApplyVirtualBackgroundOutputSchema>;

export async function applyVirtualBackground(input: ApplyVirtualBackgroundInput): Promise<ApplyVirtualBackgroundOutput> {
  return applyVirtualBackgroundFlow(input);
}

const prompt = ai.definePrompt({
  name: 'applyVirtualBackgroundPrompt',
  input: {schema: ApplyVirtualBackgroundInputSchema},
  output: {schema: ApplyVirtualBackgroundOutputSchema},
  prompt: [
    {media: {url: '{{{photoDataUri}}}'}},
    {text: 'replace the background with'},    
    {media: {url: '{{{virtualBackgroundDataUri}}}'}},
  ],
  model: 'googleai/gemini-2.5-flash-image-preview',
  config: {
    responseModalities: ['TEXT', 'IMAGE'], // MUST provide both TEXT and IMAGE, IMAGE only won't work
  },
});

const applyVirtualBackgroundFlow = ai.defineFlow(
  {
    name: 'applyVirtualBackgroundFlow',
    inputSchema: ApplyVirtualBackgroundInputSchema,
    outputSchema: ApplyVirtualBackgroundOutputSchema,
  },
  async input => {
    const {media} = await ai.generate({
      model: 'googleai/gemini-2.5-flash-image-preview',
      prompt: [
        {media: {url: input.photoDataUri}},
        {text: 'replace the background with'},    
        {media: {url: input.virtualBackgroundDataUri}},
      ],
      config: {
        responseModalities: ['TEXT', 'IMAGE'], // MUST provide both TEXT and IMAGE, IMAGE only won't work
      },
    });

    return {modifiedPhotoDataUri: media!.url};
  }
);

































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































